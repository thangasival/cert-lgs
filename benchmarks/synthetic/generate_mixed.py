"""Generate MixedExpressive benchmark: combines CEs + axioms + SDAC.

This is the "kitchen sink" domain for Cert-LGS evaluation — all three
expressive features interact simultaneously, exercising the full certification
stack (C1 CE semantics, C2 partition coverage, C3 admissible bound with
axiom-derived h_cert, C4 safe pruning under SDAC).

Usage:
    python benchmarks/synthetic/generate_mixed.py \
           --out benchmarks/mixed_expressive --problems 15 --seed 42
"""

from __future__ import annotations

import argparse
import random
from pathlib import Path


def _make_domain(n_fluents: int, n_axiom_layers: int, n_cost_fluents: int) -> str:
    """Mixed domain: base fluents, axiom chain, cost-state fluents, CEs."""
    base = "\n".join(f"    (b{i})" for i in range(n_fluents))
    derived = "\n".join(f"    (d{k})" for k in range(n_axiom_layers))
    cost_fl = "\n".join(f"    (cf{i})" for i in range(n_cost_fluents))
    done_fl = "\n".join(f"    (done{i})" for i in range(n_fluents - 1))

    # Axiom chain: d0 ← b0 ∧ b1;  dk ← d(k-1) ∧ b(k+1)
    axiom_rules: list[str] = []
    for k in range(n_axiom_layers):
        if k == 0:
            body = "(and (b0) (b1))"
        else:
            extra = min(k + 1, n_fluents - 1)
            body = f"(and (d{k - 1}) (b{extra}))"
        axiom_rules.append(f"  (:derived (d{k})\n    {body})")

    # Actions: advance from bi to b(i+1), with CE + SDAC
    actions: list[str] = []
    for i in range(n_fluents - 1):
        ce_idx = (i + 2) % n_fluents
        cf_idx = i % n_cost_fluents
        actions.append(f"""\
  (:action step-{i}
    :parameters ()
    :precondition (and (b{i}) (not (done{i})))
    :effect (and (done{i})
                 (b{(i + 1) % n_fluents})
                 (not (b{i}))
                 ; conditional effect — sets next-next fluent if current CE holds
                 (when (b{ce_idx}) (b{(ce_idx + 1) % n_fluents}))
                 ; base cost
                 (increase (total-cost) 1)
                 ; state-dependent cost: +2 if cost fluent active
                 (when (cf{cf_idx}) (increase (total-cost) 2))))""")

    return f"""\
; MixedExpressive domain — generated by generate_mixed.py
; All three expressive features: conditional effects, axioms, state-dependent costs.
(define (domain mixed-expressive)
  (:requirements :conditional-effects :derived-predicates :action-costs)
  (:predicates
{base}
{derived}
{cost_fl}
{done_fl})
  (:functions (total-cost))
{chr(10).join(axiom_rules)}
{chr(10).join(actions)}
)
"""


def _make_problem(
    name: str,
    n_fluents: int,
    n_axiom_layers: int,
    n_cost_fluents: int,
    seed: int,
) -> str:
    rng = random.Random(seed)
    active_base = set(rng.sample(range(n_fluents), k=max(2, n_fluents // 2)))
    active_base.add(0)
    active_base.add(1)
    active_cf = set(rng.sample(range(n_cost_fluents), k=max(1, n_cost_fluents // 2)))

    init_base = "\n    ".join(f"(b{i})" for i in sorted(active_base))
    init_cf   = "\n    ".join(f"(cf{i})" for i in sorted(active_cf))

    goal_idx = min(n_axiom_layers - 1, rng.randint(0, n_axiom_layers - 1))
    return f"""\
; MixedExpressive problem {name}
(define (problem {name})
  (:domain mixed-expressive)
  (:init
    {init_base}
    {init_cf}
    (= (total-cost) 0))
  (:goal (d{goal_idx}))
  (:metric minimize (total-cost))
)
"""


def main() -> None:
    parser = argparse.ArgumentParser(description="Generate MixedExpressive benchmarks")
    parser.add_argument("--out", default="benchmarks/mixed_expressive")
    parser.add_argument("--problems", type=int, default=15)
    parser.add_argument("--fluents", type=int, default=8)
    parser.add_argument("--axiom-layers", type=int, default=4)
    parser.add_argument("--cost-fluents", type=int, default=3)
    parser.add_argument("--seed", type=int, default=42)
    args = parser.parse_args()

    out = Path(args.out)
    out.mkdir(parents=True, exist_ok=True)

    domain_str = _make_domain(args.fluents, args.axiom_layers, args.cost_fluents)
    (out / "domain.pddl").write_text(domain_str)
    print(f"Wrote {out / 'domain.pddl'}")

    for idx in range(args.problems):
        name = f"p{idx + 1:02d}"
        prob_str = _make_problem(
            name, args.fluents, args.axiom_layers, args.cost_fluents,
            seed=args.seed + idx,
        )
        path = out / f"{name}.pddl"
        path.write_text(prob_str)
        print(f"Wrote {path}")


if __name__ == "__main__":
    main()
