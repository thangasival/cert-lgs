"""Generate SDCostStress benchmark: stress-tests state-dependent action costs (EVMDD/SAS+).

Produces a domain where action costs depend on fluent combinations (via CEs
on cost-increase effects), and N problems of increasing cost-state complexity.

Usage:
    python benchmarks/synthetic/generate_sdcost_stress.py \
           --out benchmarks/sdcost_stress --problems 10 --seed 42
"""

from __future__ import annotations

import argparse
import random
from pathlib import Path


def _make_domain(n_cost_fluents: int, n_ops: int) -> str:
    """Domain where each action's effective cost depends on cost fluents."""
    cost_preds = "\n".join(f"    (cost-state-{i})" for i in range(n_cost_fluents))
    op_preds   = "\n".join(f"    (op-{i}-done)" for i in range(n_ops))

    actions: list[str] = []
    for k in range(n_ops):
        # Base cost 1; each cost-state-i that holds adds i+1 to cost
        ce_costs = "\n".join(
            f"             (when (cost-state-{i}) (increase (total-cost) {i + 1}))"
            for i in range(n_cost_fluents)
        )
        actions.append(f"""\
  (:action do-op-{k}
    :parameters ()
    :precondition (not (op-{k}-done))
    :effect (and (op-{k}-done)
                 (increase (total-cost) 1)
{ce_costs}))""")

    return f"""\
; SDCostStress domain — generated by generate_sdcost_stress.py
; Purpose: stress-test state-dependent cost handling in certifiers C3 and C4.
(define (domain sdcost-stress)
  (:requirements :conditional-effects :action-costs)
  (:predicates
{cost_preds}
{op_preds})
  (:functions (total-cost))
{chr(10).join(actions)}
)
"""


def _make_problem(
    name: str,
    n_cost_fluents: int,
    n_ops: int,
    active_cost_fluents: int,
    seed: int,
) -> str:
    rng = random.Random(seed)
    active = set(rng.sample(range(n_cost_fluents), k=min(active_cost_fluents, n_cost_fluents)))
    init_lines = "\n    ".join(f"(cost-state-{i})" for i in sorted(active))
    # Goal: complete a subset of operations
    n_goal = max(1, n_ops // 2)
    goal_ops = rng.sample(range(n_ops), k=n_goal)
    goal_lines = "\n             ".join(f"(op-{k}-done)" for k in goal_ops)
    return f"""\
; SDCostStress problem {name}
(define (problem {name})
  (:domain sdcost-stress)
  (:init
    {init_lines}
    (= (total-cost) 0))
  (:goal (and {goal_lines}))
  (:metric minimize (total-cost))
)
"""


def main() -> None:
    parser = argparse.ArgumentParser(description="Generate SDCostStress benchmarks")
    parser.add_argument("--out", default="benchmarks/sdcost_stress")
    parser.add_argument("--problems", type=int, default=10)
    parser.add_argument("--cost-fluents", type=int, default=5,
                        help="Number of cost-state fluents in domain")
    parser.add_argument("--ops", type=int, default=8, help="Number of operators")
    parser.add_argument("--seed", type=int, default=42)
    args = parser.parse_args()

    out = Path(args.out)
    out.mkdir(parents=True, exist_ok=True)

    domain_str = _make_domain(args.cost_fluents, args.ops)
    (out / "domain.pddl").write_text(domain_str)
    print(f"Wrote {out / 'domain.pddl'}")

    for idx in range(args.problems):
        active = 1 + idx % args.cost_fluents
        name = f"p{idx + 1:02d}_cf{active:02d}"
        prob_str = _make_problem(
            name, args.cost_fluents, args.ops, active, seed=args.seed + idx
        )
        path = out / f"{name}.pddl"
        path.write_text(prob_str)
        print(f"Wrote {path}")


if __name__ == "__main__":
    main()
