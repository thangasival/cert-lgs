"""Generate AxiomStress benchmark: stress-tests axiom closure (derived predicates).

Produces a domain with layered :derived rules and N problems of increasing depth.
Derived predicate chains of length D require D rounds of the fixed-point iteration.

Usage:
    python benchmarks/synthetic/generate_axiom_stress.py --out benchmarks/axiom_stress \
           --problems 10 --min-depth 2 --max-depth 12 --seed 42
"""

from __future__ import annotations

import argparse
import random
from pathlib import Path


def _make_domain(max_depth: int, fan_out: int) -> str:
    """Return PDDL domain with a chain of derived predicates of length max_depth.

    Each layer l derives pred_l if any of the fan_out source predicates hold.
    Base predicates: base_0 … base_{fan_out-1}
    Derived chain:   derived_0 ← base_0 ∧ base_1
                     derived_1 ← derived_0 ∧ base_2
                     ...
                     derived_{max_depth-1} ← derived_{max_depth-2} ∧ base_{(max_depth+1) % fan_out}
    """
    n_base = max(fan_out, max_depth + 2)

    base_preds = "\n".join(f"    (base-{i})" for i in range(n_base))
    derived_preds = "\n".join(f"    (derived-{d})" for d in range(max_depth))

    # Actions: toggle base fluents
    actions: list[str] = []
    for i in range(n_base - 1):
        actions.append(f"""\
  (:action toggle-base-{i}
    :parameters ()
    :precondition (base-{i})
    :effect (and (not (base-{i})) (base-{i + 1})
                 (increase (total-cost) 1)))""")

    # Derived chain
    derived_rules: list[str] = []
    for d in range(max_depth):
        if d == 0:
            body = f"(and (base-0) (base-1))"
        else:
            extra_base = (d + 1) % n_base
            body = f"(and (derived-{d - 1}) (base-{extra_base}))"
        derived_rules.append(f"""\
  (:derived (derived-{d})
    {body})""")

    return f"""\
; AxiomStress domain — generated by generate_axiom_stress.py
; Purpose: stress-test fixed-point axiom closure in ExpressiveSemantics.
(define (domain axiom-stress)
  (:requirements :derived-predicates :action-costs)
  (:predicates
{base_preds}
{derived_preds})
  (:functions (total-cost))
{chr(10).join(derived_rules)}
{chr(10).join(actions)}
)
"""


def _make_problem(name: str, depth: int, max_depth: int, n_base: int, seed: int) -> str:
    """Goal: achieve derived-{depth-1}, which requires the full chain of length depth."""
    rng = random.Random(seed)
    # Randomly initialise base predicates — but ensure depth-chain can fire
    # by always setting base-0 and base-1 true.
    forced = {0, 1}
    extra = set(rng.sample(range(2, n_base), k=min(depth, n_base - 2)))
    active = forced | extra
    init_lines = "\n    ".join(f"(base-{i})" for i in sorted(active))
    goal_derived = max(0, depth - 1)
    return f"""\
; AxiomStress problem {name}
(define (problem {name})
  (:domain axiom-stress)
  (:init
    {init_lines}
    (= (total-cost) 0))
  (:goal (derived-{goal_derived}))
  (:metric minimize (total-cost))
)
"""


def main() -> None:
    parser = argparse.ArgumentParser(description="Generate AxiomStress benchmarks")
    parser.add_argument("--out", default="benchmarks/axiom_stress")
    parser.add_argument("--problems", type=int, default=10)
    parser.add_argument("--min-depth", type=int, default=2)
    parser.add_argument("--max-depth", type=int, default=12)
    parser.add_argument("--fan-out", type=int, default=3,
                        help="Number of base predicates per derived rule")
    parser.add_argument("--seed", type=int, default=42)
    args = parser.parse_args()

    out = Path(args.out)
    out.mkdir(parents=True, exist_ok=True)

    n_base = max(args.fan_out, args.max_depth + 2)
    domain_str = _make_domain(args.max_depth, args.fan_out)
    (out / "domain.pddl").write_text(domain_str)
    print(f"Wrote {out / 'domain.pddl'}")

    step = max(1, (args.max_depth - args.min_depth) // args.problems)
    for idx in range(args.problems):
        depth = min(args.min_depth + idx * step, args.max_depth)
        name = f"p{idx + 1:02d}_d{depth:03d}"
        prob_str = _make_problem(name, depth, args.max_depth, n_base, seed=args.seed + idx)
        path = out / f"{name}.pddl"
        path.write_text(prob_str)
        print(f"Wrote {path}")


if __name__ == "__main__":
    main()
