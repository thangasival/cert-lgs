"""Generate CEffStress benchmark: stress-tests conditional-effect certification (C1/C2).

Produces a parameterised domain + N problem files of increasing size.
All actions carry one or more conditional effects, systematically varying:
  - branching factor (number of CE branches per action)
  - state-space width (number of fluents)

Usage:
    python benchmarks/synthetic/generate_ceff_stress.py --out benchmarks/ceff_stress \
           --problems 10 --min-fluents 4 --max-fluents 20 --seed 42
"""

from __future__ import annotations

import argparse
import random
from pathlib import Path


# ---------------------------------------------------------------------------
# Domain generation
# ---------------------------------------------------------------------------

def _make_domain(max_fluents: int, branches_per_action: int) -> str:
    """Return a PDDL domain string for the CEffStress benchmark."""
    preds = "\n".join(f"    (f{i})" for i in range(max_fluents))
    funcs = "    (total-cost)"

    actions: list[str] = []
    # One action per pair of fluents; each action has `branches_per_action` CEs
    for i in range(max_fluents - 1):
        j = (i + 1) % max_fluents
        ce_lines: list[str] = []
        for b in range(branches_per_action):
            cond_idx = (i + b + 2) % max_fluents
            eff_idx  = (j + b + 1) % max_fluents
            ce_lines.append(
                f"             (when (f{cond_idx}) (f{eff_idx}))"
            )
        conds_str = "\n".join(ce_lines)
        actions.append(f"""\
  (:action act-{i}-{j}
    :parameters ()
    :precondition (f{i})
    :effect (and (not (f{i})) (f{j})
                 (increase (total-cost) 1)
{conds_str}))""")

    return f"""\
; CEffStress domain — generated by generate_ceff_stress.py
; Purpose: stress-test conditional-effect handling in certifiers C1 and C2.
(define (domain ceff-stress)
  (:requirements :conditional-effects :action-costs)
  (:predicates
{preds})
  (:functions
{funcs})
{chr(10).join(actions)}
)
"""


# ---------------------------------------------------------------------------
# Problem generation
# ---------------------------------------------------------------------------

def _make_problem(
    name: str,
    n_fluents: int,
    max_fluents: int,
    seed: int,
) -> str:
    """Return a PDDL problem with n_fluents active init fluents."""
    rng = random.Random(seed)
    active = rng.sample(range(max_fluents), k=min(n_fluents, max_fluents))
    inactive = [i for i in range(max_fluents) if i not in active]
    init_lines = "\n    ".join(f"(f{i})" for i in active)
    # Goal: flip the first inactive fluent on (if any), or toggle the last active one
    if inactive:
        goal_fluent = inactive[0]
    else:
        goal_fluent = (active[-1] + 1) % max_fluents
    return f"""\
; CEffStress problem {name}
(define (problem {name})
  (:domain ceff-stress)
  (:init
    {init_lines}
    (= (total-cost) 0))
  (:goal (f{goal_fluent}))
  (:metric minimize (total-cost))
)
"""


# ---------------------------------------------------------------------------
# CLI
# ---------------------------------------------------------------------------

def main() -> None:
    parser = argparse.ArgumentParser(description="Generate CEffStress benchmarks")
    parser.add_argument("--out", default="benchmarks/ceff_stress", help="Output directory")
    parser.add_argument("--problems", type=int, default=10, help="Number of problem files")
    parser.add_argument("--min-fluents", type=int, default=4)
    parser.add_argument("--max-fluents", type=int, default=20)
    parser.add_argument("--branches", type=int, default=2,
                        help="Conditional-effect branches per action")
    parser.add_argument("--seed", type=int, default=42)
    args = parser.parse_args()

    out = Path(args.out)
    out.mkdir(parents=True, exist_ok=True)

    domain_str = _make_domain(args.max_fluents, args.branches)
    (out / "domain.pddl").write_text(domain_str)
    print(f"Wrote {out / 'domain.pddl'}")

    step = max(1, (args.max_fluents - args.min_fluents) // args.problems)
    for idx in range(args.problems):
        n = min(args.min_fluents + idx * step, args.max_fluents)
        name = f"p{idx + 1:02d}_f{n:03d}"
        prob_str = _make_problem(name, n, args.max_fluents, seed=args.seed + idx)
        path = out / f"{name}.pddl"
        path.write_text(prob_str)
        print(f"Wrote {path}")


if __name__ == "__main__":
    main()
